-- Enterprise AI Business Intelligence & Analytics Platform
-- MySQL schema dump  --  ABC Corporation
--
-- CONVENTION: every TABLE name and every COLUMN name is UPPERCASE.
--   This is a hard requirement of the platform: the Schema-RAG catalog, the
--   SQL-generation agent, and the SQL guard all assume UPPERCASE identifiers,
--   so the generated SQL, the retrieved schema context, and the physical
--   schema line up exactly (no case-folding ambiguity across engines).
--
-- Engine/charset are pinned for deterministic behaviour across environments.
-- Load order: 01_schema.sql then 02_seed.sql.
-- =============================================================================

SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;
SET @OLD_SQL_MODE = @@SQL_MODE;
SET SQL_MODE = 'STRICT_ALL_TABLES,NO_ENGINE_SUBSTITUTION';

CREATE DATABASE IF NOT EXISTS ENTERPRISE_BI
    DEFAULT CHARACTER SET utf8mb4
    DEFAULT COLLATE utf8mb4_unicode_ci;
USE ENTERPRISE_BI;

-- -----------------------------------------------------------------------------
-- REGIONS / BRANCHES  (organisational geography)
-- -----------------------------------------------------------------------------
DROP TABLE IF EXISTS REGIONS;
CREATE TABLE REGIONS (
    REGION_ID       INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    REGION_NAME     VARCHAR(64)    NOT NULL,
    COUNTRY         VARCHAR(64)    NOT NULL DEFAULT 'India',
    CREATED_AT      DATETIME       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (REGION_ID),
    UNIQUE KEY UQ_REGIONS_NAME (REGION_NAME)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS BRANCHES;
CREATE TABLE BRANCHES (
    BRANCH_ID       INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    BRANCH_NAME     VARCHAR(96)    NOT NULL,
    REGION_ID       INT UNSIGNED   NOT NULL,
    STATE           VARCHAR(64)    NOT NULL,
    CITY            VARCHAR(64)    NOT NULL,
    OPENED_DATE     DATE           NULL,
    IS_ACTIVE       TINYINT(1)     NOT NULL DEFAULT 1,
    CREATED_AT      DATETIME       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (BRANCH_ID),
    UNIQUE KEY UQ_BRANCHES_NAME (BRANCH_NAME),
    KEY IX_BRANCHES_REGION (REGION_ID),
    KEY IX_BRANCHES_STATE (STATE),
    CONSTRAINT FK_BRANCHES_REGION FOREIGN KEY (REGION_ID) REFERENCES REGIONS (REGION_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- -----------------------------------------------------------------------------
-- PRODUCTS  (catalogue used by SALES / INVENTORY / PROCUREMENT)
-- -----------------------------------------------------------------------------
DROP TABLE IF EXISTS PRODUCTS;
CREATE TABLE PRODUCTS (
    PRODUCT_ID      INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    PRODUCT_NAME    VARCHAR(128)   NOT NULL,
    CATEGORY        VARCHAR(64)    NOT NULL,
    UNIT_PRICE      DECIMAL(12,2)  NOT NULL DEFAULT 0.00,
    IS_ACTIVE       TINYINT(1)     NOT NULL DEFAULT 1,
    PRIMARY KEY (PRODUCT_ID),
    KEY IX_PRODUCTS_CATEGORY (CATEGORY)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- -----------------------------------------------------------------------------
-- CUSTOMERS / CUSTOMER_ACCOUNTS
-- -----------------------------------------------------------------------------
DROP TABLE IF EXISTS CUSTOMERS;
CREATE TABLE CUSTOMERS (
    CUSTOMER_ID     INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    CUSTOMER_NAME   VARCHAR(128)   NOT NULL,
    STATE           VARCHAR(64)    NOT NULL,
    CITY            VARCHAR(64)    NULL,
    SEGMENT         VARCHAR(32)    NOT NULL DEFAULT 'RETAIL',
    BRANCH_ID       INT UNSIGNED   NOT NULL,
    CREATED_AT      DATETIME       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (CUSTOMER_ID),
    KEY IX_CUSTOMERS_STATE (STATE),
    KEY IX_CUSTOMERS_BRANCH (BRANCH_ID),
    CONSTRAINT FK_CUSTOMERS_BRANCH FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES (BRANCH_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS CUSTOMER_ACCOUNTS;
CREATE TABLE CUSTOMER_ACCOUNTS (
    ACCOUNT_ID          INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    CUSTOMER_ID         INT UNSIGNED   NOT NULL,
    ACCOUNT_STATUS      VARCHAR(32)    NOT NULL DEFAULT 'ACTIVE',
    CREDIT_LIMIT        DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    OUTSTANDING_BALANCE DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    OPENED_DATE         DATE           NOT NULL,
    PRIMARY KEY (ACCOUNT_ID),
    KEY IX_ACCOUNTS_CUSTOMER (CUSTOMER_ID),
    CONSTRAINT FK_ACCOUNTS_CUSTOMER FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS (CUSTOMER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- -----------------------------------------------------------------------------
-- EMPLOYEES / EMPLOYEE_PERFORMANCE
-- -----------------------------------------------------------------------------
DROP TABLE IF EXISTS EMPLOYEES;
CREATE TABLE EMPLOYEES (
    EMPLOYEE_ID     INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    EMPLOYEE_NAME   VARCHAR(128)   NOT NULL,
    BRANCH_ID       INT UNSIGNED   NOT NULL,
    DEPARTMENT      VARCHAR(64)    NOT NULL,
    DESIGNATION     VARCHAR(64)    NOT NULL,
    HIRE_DATE       DATE           NOT NULL,
    IS_ACTIVE       TINYINT(1)     NOT NULL DEFAULT 1,
    PRIMARY KEY (EMPLOYEE_ID),
    KEY IX_EMPLOYEES_BRANCH (BRANCH_ID),
    KEY IX_EMPLOYEES_DEPARTMENT (DEPARTMENT),
    CONSTRAINT FK_EMPLOYEES_BRANCH FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES (BRANCH_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS EMPLOYEE_PERFORMANCE;
CREATE TABLE EMPLOYEE_PERFORMANCE (
    PERFORMANCE_ID  INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    EMPLOYEE_ID     INT UNSIGNED   NOT NULL,
    FISCAL_QUARTER  VARCHAR(8)     NOT NULL,   -- e.g. 'Q1', 'Q2'
    FISCAL_YEAR     SMALLINT       NOT NULL,
    SALES_TARGET    DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    SALES_ACHIEVED  DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    PERFORMANCE_SCORE DECIMAL(5,2) NOT NULL DEFAULT 0.00,
    PRIMARY KEY (PERFORMANCE_ID),
    KEY IX_EMPPERF_EMPLOYEE (EMPLOYEE_ID),
    KEY IX_EMPPERF_PERIOD (FISCAL_YEAR, FISCAL_QUARTER),
    CONSTRAINT FK_EMPPERF_EMPLOYEE FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES (EMPLOYEE_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- -----------------------------------------------------------------------------
-- COLLECTIONS  (customer collections — the platform's primary metric)
-- -----------------------------------------------------------------------------
DROP TABLE IF EXISTS COLLECTIONS;
CREATE TABLE COLLECTIONS (
    COLLECTION_ID       INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    BRANCH_ID           INT UNSIGNED   NOT NULL,
    CUSTOMER_ID         INT UNSIGNED   NOT NULL,
    COLLECTION_DATE     DATE           NOT NULL,
    COLLECTION_MONTH    CHAR(7)        NOT NULL,   -- 'YYYY-MM' for fast trend grouping
    COLLECTION_AMOUNT   DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    PAYMENT_MODE        VARCHAR(32)    NOT NULL DEFAULT 'BANK_TRANSFER',
    CREATED_AT          DATETIME       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (COLLECTION_ID),
    KEY IX_COLLECTIONS_BRANCH (BRANCH_ID),
    KEY IX_COLLECTIONS_CUSTOMER (CUSTOMER_ID),
    KEY IX_COLLECTIONS_DATE (COLLECTION_DATE),
    KEY IX_COLLECTIONS_MONTH (COLLECTION_MONTH),
    CONSTRAINT FK_COLLECTIONS_BRANCH FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES (BRANCH_ID),
    CONSTRAINT FK_COLLECTIONS_CUSTOMER FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS (CUSTOMER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- -----------------------------------------------------------------------------
-- REVENUE  (monthly revenue by branch)
-- -----------------------------------------------------------------------------
DROP TABLE IF EXISTS REVENUE;
CREATE TABLE REVENUE (
    REVENUE_ID      INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    BRANCH_ID       INT UNSIGNED   NOT NULL,
    REVENUE_MONTH   CHAR(7)        NOT NULL,   -- 'YYYY-MM'
    REVENUE_AMOUNT  DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    COST_AMOUNT     DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    GROSS_MARGIN    DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    PRIMARY KEY (REVENUE_ID),
    UNIQUE KEY UQ_REVENUE_BRANCH_MONTH (BRANCH_ID, REVENUE_MONTH),
    KEY IX_REVENUE_MONTH (REVENUE_MONTH),
    CONSTRAINT FK_REVENUE_BRANCH FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES (BRANCH_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- -----------------------------------------------------------------------------
-- SALES  (line-item sales)
-- -----------------------------------------------------------------------------
DROP TABLE IF EXISTS SALES;
CREATE TABLE SALES (
    SALE_ID         INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    BRANCH_ID       INT UNSIGNED   NOT NULL,
    PRODUCT_ID      INT UNSIGNED   NOT NULL,
    EMPLOYEE_ID     INT UNSIGNED   NULL,
    CUSTOMER_ID     INT UNSIGNED   NULL,
    SALE_DATE       DATE           NOT NULL,
    QUANTITY        INT UNSIGNED   NOT NULL DEFAULT 1,
    UNIT_PRICE      DECIMAL(12,2)  NOT NULL DEFAULT 0.00,
    SALE_AMOUNT     DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    PRIMARY KEY (SALE_ID),
    KEY IX_SALES_BRANCH (BRANCH_ID),
    KEY IX_SALES_PRODUCT (PRODUCT_ID),
    KEY IX_SALES_DATE (SALE_DATE),
    CONSTRAINT FK_SALES_BRANCH FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES (BRANCH_ID),
    CONSTRAINT FK_SALES_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS (PRODUCT_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- -----------------------------------------------------------------------------
-- CLAIMS / PAYMENTS  (finance)
-- -----------------------------------------------------------------------------
DROP TABLE IF EXISTS CLAIMS;
CREATE TABLE CLAIMS (
    CLAIM_ID        INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    CUSTOMER_ID     INT UNSIGNED   NOT NULL,
    BRANCH_ID       INT UNSIGNED   NOT NULL,
    CLAIM_DATE      DATE           NOT NULL,
    CLAIM_AMOUNT    DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    CLAIM_STATUS    VARCHAR(32)    NOT NULL DEFAULT 'SUBMITTED',  -- SUBMITTED/APPROVED/DENIED/PAID
    SETTLED_AMOUNT  DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    PRIMARY KEY (CLAIM_ID),
    KEY IX_CLAIMS_CUSTOMER (CUSTOMER_ID),
    KEY IX_CLAIMS_BRANCH (BRANCH_ID),
    KEY IX_CLAIMS_STATUS (CLAIM_STATUS),
    CONSTRAINT FK_CLAIMS_CUSTOMER FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS (CUSTOMER_ID),
    CONSTRAINT FK_CLAIMS_BRANCH FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES (BRANCH_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS PAYMENTS;
CREATE TABLE PAYMENTS (
    PAYMENT_ID      INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    CLAIM_ID        INT UNSIGNED   NULL,
    CUSTOMER_ID     INT UNSIGNED   NOT NULL,
    BRANCH_ID       INT UNSIGNED   NOT NULL,
    PAYMENT_DATE    DATE           NOT NULL,
    PAYMENT_AMOUNT  DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    PAYMENT_MODE    VARCHAR(32)    NOT NULL DEFAULT 'BANK_TRANSFER',
    PRIMARY KEY (PAYMENT_ID),
    KEY IX_PAYMENTS_CLAIM (CLAIM_ID),
    KEY IX_PAYMENTS_CUSTOMER (CUSTOMER_ID),
    KEY IX_PAYMENTS_DATE (PAYMENT_DATE),
    CONSTRAINT FK_PAYMENTS_CLAIM FOREIGN KEY (CLAIM_ID) REFERENCES CLAIMS (CLAIM_ID),
    CONSTRAINT FK_PAYMENTS_CUSTOMER FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS (CUSTOMER_ID),
    CONSTRAINT FK_PAYMENTS_BRANCH FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES (BRANCH_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- -----------------------------------------------------------------------------
-- INVENTORY / PROCUREMENT
-- -----------------------------------------------------------------------------
DROP TABLE IF EXISTS INVENTORY;
CREATE TABLE INVENTORY (
    INVENTORY_ID    INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    BRANCH_ID       INT UNSIGNED   NOT NULL,
    PRODUCT_ID      INT UNSIGNED   NOT NULL,
    QUANTITY_ON_HAND INT           NOT NULL DEFAULT 0,
    REORDER_LEVEL   INT            NOT NULL DEFAULT 0,
    LAST_RESTOCK_DATE DATE         NULL,
    PRIMARY KEY (INVENTORY_ID),
    UNIQUE KEY UQ_INVENTORY_BRANCH_PRODUCT (BRANCH_ID, PRODUCT_ID),
    CONSTRAINT FK_INVENTORY_BRANCH FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES (BRANCH_ID),
    CONSTRAINT FK_INVENTORY_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS (PRODUCT_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS PROCUREMENT;
CREATE TABLE PROCUREMENT (
    PURCHASE_ORDER_ID INT UNSIGNED NOT NULL AUTO_INCREMENT,
    BRANCH_ID       INT UNSIGNED   NOT NULL,
    PRODUCT_ID      INT UNSIGNED   NOT NULL,
    SUPPLIER_NAME   VARCHAR(128)   NOT NULL,
    ORDER_DATE      DATE           NOT NULL,
    QUANTITY_ORDERED INT           NOT NULL DEFAULT 0,
    ORDER_AMOUNT    DECIMAL(14,2)  NOT NULL DEFAULT 0.00,
    ORDER_STATUS    VARCHAR(32)    NOT NULL DEFAULT 'OPEN',  -- OPEN/RECEIVED/CANCELLED
    PRIMARY KEY (PURCHASE_ORDER_ID),
    KEY IX_PROCUREMENT_BRANCH (BRANCH_ID),
    KEY IX_PROCUREMENT_PRODUCT (PRODUCT_ID),
    CONSTRAINT FK_PROCUREMENT_BRANCH FOREIGN KEY (BRANCH_ID) REFERENCES BRANCHES (BRANCH_ID),
    CONSTRAINT FK_PROCUREMENT_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS (PRODUCT_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- -----------------------------------------------------------------------------
-- PLATFORM-OWNED TABLES  (auth + audit; not business data)
-- -----------------------------------------------------------------------------
DROP TABLE IF EXISTS ROLES;
CREATE TABLE ROLES (
    ROLE_ID         INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    ROLE_NAME       VARCHAR(32)    NOT NULL,  -- BUSINESS_ANALYST/MANAGER/FINANCE/SALES/ADMIN
    DESCRIPTION     VARCHAR(255)   NULL,
    PRIMARY KEY (ROLE_ID),
    UNIQUE KEY UQ_ROLES_NAME (ROLE_NAME)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS USERS;
CREATE TABLE USERS (
    USER_ID         INT UNSIGNED   NOT NULL AUTO_INCREMENT,
    USERNAME        VARCHAR(64)    NOT NULL,
    EMAIL           VARCHAR(128)   NOT NULL,
    ROLE_ID         INT UNSIGNED   NOT NULL,
    PASSWORD_HASH   VARCHAR(255)   NOT NULL,
    IS_ACTIVE       TINYINT(1)     NOT NULL DEFAULT 1,
    CREATED_AT      DATETIME       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (USER_ID),
    UNIQUE KEY UQ_USERS_USERNAME (USERNAME),
    UNIQUE KEY UQ_USERS_EMAIL (EMAIL),
    CONSTRAINT FK_USERS_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLES (ROLE_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

DROP TABLE IF EXISTS AUDIT_LOG;
CREATE TABLE AUDIT_LOG (
    AUDIT_ID            BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    USER_ID             INT UNSIGNED    NULL,
    USERNAME            VARCHAR(64)     NULL,
    ROLE_NAME           VARCHAR(32)     NULL,
    REQUEST_ID          VARCHAR(64)     NOT NULL,
    PROMPT              TEXT            NOT NULL,
    GENERATED_SQL       TEXT            NULL,
    INTENT              VARCHAR(64)     NULL,
    EXECUTION_MS        INT             NULL,
    ROW_COUNT           INT             NULL,
    CHART_KIND          VARCHAR(32)     NULL,
    REPORT_FORMAT       VARCHAR(16)     NULL,
    STATUS              VARCHAR(32)     NOT NULL DEFAULT 'OK',  -- OK/BLOCKED/ERROR
    ERROR_MESSAGE       VARCHAR(512)    NULL,
    CREATED_AT          DATETIME        NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (AUDIT_ID),
    KEY IX_AUDIT_USER (USER_ID),
    KEY IX_AUDIT_CREATED (CREATED_AT),
    KEY IX_AUDIT_REQUEST (REQUEST_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

SET FOREIGN_KEY_CHECKS = 1;
SET SQL_MODE = @OLD_SQL_MODE;
-- End of schema.
